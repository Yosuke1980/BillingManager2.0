def add_cast_members():
    """出演者を一括追加"""
    conn = sqlite3.connect('order_management.db')
    cursor = conn.cursor()

    try:
        added_count = 0
        skipped_count = 0

        print(f"出演者の追加を開始します（全{len(CAST_MEMBERS)}名）\n")

        for cast_name in CAST_MEMBERS:
            # 既存チェック
            cursor.execute("""
                SELECT id FROM partners
                WHERE name = ? AND partner_type = '出演者'
            """, (cast_name,))

            if cursor.fetchone():
                print(f"  スキップ（既存）: {cast_name}")
                skipped_count += 1
                continue

            # 新規追加
            cursor.execute("""
                INSERT INTO partners (name, partner_type, notes, created_at, updated_at)
                VALUES (?, ?, ?, CURRENT_TIMESTAMP, CURRENT_TIMESTAMP)
            """, (cast_name, "出演者", "出演者リストから一括追加"))

            print(f"  追加: {cast_name}")
            added_count += 1

        conn.commit()

        print(f"\n✓ 出演者の追加が完了しました")
        print(f"  追加: {added_count}名")
        print(f"  スキップ（既存）: {skipped_count}名")

        # 現在の出演者数を確認
        cursor.execute("""
            SELECT COUNT(*) FROM partners
            WHERE partner_type = '出演者'
        """)
        total_count = cursor.fetchone()[0]
        print(f"\n現在の出演者総数: {total_count}名")

    except Exception as e:
        conn.rollback()
        print(f"エラー: {e}")
        import traceback
        traceback.print_exc()
    finally:
        conn.close()


if __name__ == "__main__":
    add_cast_members()
